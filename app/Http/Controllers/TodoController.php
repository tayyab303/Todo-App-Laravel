    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $rules = [
            'task' => 'required|regex:/^[a-z0-9\s]+$/i|min:5|max:100',
        ];
    
        $customMessages = [
            'task.required' => 'The :attribute field is required.',
            'task.regex' => 'The :attribute should be string',
            'task.min' => 'The :attribute should be greater then 5 character',
            'task.max' => 'The :attribute should be less then 100 character',
        ];

        $this->validate($request, $rules, $customMessages);
        // dd($request->title);
        Todo::create(['task' => $request->task]);
        return redirect('todo/create')->with('message', 'Task Added Successfully!');
    }

